/**
 * Export a Markdown impact analysis report.
 * @param {object} impactData — from POST /api/impact
 * @param {string} fileName — the analyzed file name
 * @param {string} repoPath
 */
export function exportImpactReport(impactData, fileName, repoPath) {
  if (!impactData) return;

  const name = repoName(repoPath);
  const lines = [
    `# Impact Analysis — ${fileName}`,
    '',
    `> Repository: ${name}`,
    `> Generated on ${now()}`,
    '',
    '---',
    '',
    '## Summary',
    '',
    `| Metric | Value |`,
    `|--------|-------|`,
    `| Risk Level | **${impactData.risk}** |`,
    `| Direct Dependents | ${impactData.direct} |`,
    `| Transitive Dependents | ${impactData.transitive} |`,
    '',
  ];

  // Dependents table
  lines.push('## Critical Dependents', '');
  if (impactData.dependents?.length > 0) {
    lines.push('| # | File | Criticality |');
    lines.push('|---|------|-------------|');
    impactData.dependents.forEach((dep, i) => {
      lines.push(`| ${i + 1} | \`${dep.file}\` | ${dep.score.toFixed(1)} |`);
    });
  } else {
    lines.push('No dependents found.');
  }
  lines.push('');
  lines.push('---', '', `*Report generated by CNI*`);

  const safeName = fileName.replace(/[^a-zA-Z0-9._-]/g, '_');
  downloadFile(lines.join('\n'), `impact-${safeName}-${today()}.md`);
}

