/**
 * lib/exportReport.js — Export analysis data as Markdown or JSON files.
 *
 * Pure utility functions — no React hooks or 'use client' needed.
 */

// ---------------------------------------------------------------------------
// Helper
// ---------------------------------------------------------------------------

function downloadFile(content, filename, type = 'text/markdown') {
  const blob = new Blob([content], { type });
  const url = URL.createObjectURL(blob);
  const a = document.createElement('a');
  a.href = url;
  a.download = filename;
  a.click();
  URL.revokeObjectURL(url);
}

function today() {
  return new Date().toISOString().slice(0, 10);
}

function repoName(repoPath) {
  if (!repoPath) return 'unknown';
  return repoPath.replace(/\\/g, '/').split('/').filter(Boolean).pop() || 'repo';
}

function now() {
  return new Date().toLocaleString();
}

