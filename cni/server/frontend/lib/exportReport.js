// ---------------------------------------------------------------------------

/**
 * Export a Markdown health report.
 * @param {object} healthData — from GET /api/health
 * @param {string} repoPath
 */
export function exportHealthReport(healthData, repoPath) {
  if (!healthData) return;

  const name = repoName(repoPath);
  const lines = [
    `# Health Report — ${name}`,
    '',
    `> Generated on ${now()}`,
    '',
    '---',
    '',
    '## Overall Score',
    '',
    `**${healthData.score} / 100**`,
    '',
    `Total modules analyzed: ${healthData.total_modules || 'N/A'}`,
    '',
  ];

  // God Modules
  lines.push('## God Modules', '');
  if (healthData.god_modules?.length > 0) {
    lines.push('| File | In-degree | Severity |');
    lines.push('|------|-----------|----------|');
    healthData.god_modules.forEach((m) => {
      lines.push(`| \`${m.file}\` | ${m.value} | ${m.severity} |`);
    });
  } else {
    lines.push('No god modules detected. ✓');
  }
  lines.push('');

  // Coupled Modules
  lines.push('## Coupled Modules', '');
  if (healthData.coupled_modules?.length > 0) {
    lines.push('| File | Out-degree | Severity |');
    lines.push('|------|------------|----------|');
    healthData.coupled_modules.forEach((m) => {
      lines.push(`| \`${m.file}\` | ${m.value} | ${m.severity} |`);
    });
  } else {
    lines.push('No coupled modules detected. ✓');
  }
  lines.push('');

  // Isolated
  lines.push('## Isolated Modules', '');
  lines.push(`Count: **${healthData.isolated_count ?? 0}**`);
  lines.push('');
  lines.push('---', '', `*Report generated by CNI*`);

  downloadFile(lines.join('\n'), `health-report-${today()}.md`);
}

