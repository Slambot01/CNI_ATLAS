/**
 * Export a Markdown onboarding guide.
 * @param {object} onboardData — from GET /api/onboard
 * @param {string} repoPath
 */
export function exportOnboardReport(onboardData, repoPath) {
  if (!onboardData) return;

  const name = repoName(repoPath);
  const lines = [
    `# Onboarding Guide — ${name}`,
    '',
    `> Generated on ${now()}`,
    '',
    '---',
    '',
  ];

  // Entry points
  lines.push('## Entry Points', '');
  if (onboardData.entry_points?.length > 0) {
    onboardData.entry_points.forEach((ep) => {
      const short = ep.split(/[/\\]/).pop();
      lines.push(`- \`${short}\``);
    });
  } else {
    lines.push('No entry points detected.');
  }
  lines.push('');

  // Critical modules
  lines.push('## Critical Modules', '');
  if (onboardData.critical_modules?.length > 0) {
    lines.push('| # | Module | Centrality |');
    lines.push('|---|--------|------------|');
    onboardData.critical_modules.forEach((mod, i) => {
      lines.push(`| ${i + 1} | \`${mod.name}\` | ${mod.centrality.toFixed(3)} |`);
    });
  } else {
    lines.push('No critical modules detected.');
  }
  lines.push('');

  // Dead modules
  if (onboardData.dead_modules?.length > 0) {
    lines.push('## Dead Modules', '');
    onboardData.dead_modules.forEach((dm) => {
      lines.push(`- \`${dm}\``);
    });
    lines.push('');
  }

  // Architecture summary
  if (onboardData.summary) {
    lines.push('## Architecture Summary', '');
    lines.push(onboardData.summary);
    lines.push('');
  }

  lines.push('---', '', `*Report generated by CNI*`);

  downloadFile(lines.join('\n'), `onboarding-guide-${today()}.md`);
}

